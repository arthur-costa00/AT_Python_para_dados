class Movie:
    def __init__(self, titulo, ano, nota):
        self.titulo = titulo
        self.ano = ano
        self.nota = nota

class Series:
    def __init__(self, titulo, ano, temporadas):
        self.titulo = titulo
        self.ano = ano
        self.temporadas = temporadas

class TV:
    def __init__(self, titulo, canal):
        self.titulo = titulo
        self.canal = canal
